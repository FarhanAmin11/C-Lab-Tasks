#include <iostream>
using namespace std;

int main() {
    int num1, num2, sum;

    // Input two integers
    cout << "Enter first integer: ";
    cin >> num1;

    cout << "Enter second integer: ";
    cin >> num2;

    // Calculate sum
    sum = num1 + num2;

    // Display result
    cout << "Sum = " << sum << endl;

    return 0;
}